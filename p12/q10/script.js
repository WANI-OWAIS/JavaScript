// 1️setTimeout
// Meaning

// Runs a function once after a specified delay.

// Example
setTimeout(function(){
  console.log("Hello after 3 seconds");
},3000);


// 2️⃣setInterval
// Meaning

// Runs a function repeatedly after a fixed interval.

// Example
setInterval(function(){
  console.log("Hello every 2 seconds");
},2000);